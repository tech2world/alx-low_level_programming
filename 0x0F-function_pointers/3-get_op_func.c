#include "3-calc.h"

/**
  * get_op_func- selects correct func to perform operation.
  * @s: opeartor passed as argument.
  *
  * Return: pointer to the operator that corresponds to the given as param.
  */

int (*get_op_func(char *s))(int, int)
{
	op_t ops[] = {
		{"+", op_add},
		{"-", op_sub},
		{"*", op_mul},
		{"/", op_div},
		{"%", op_mod},
		{NULL, NULL}
	};

	int i = 0;

	while (ops[i].op)
	{
		if (strcmp(ops[i].op, s) == 0)
			return (ops[i].f);
		i++;
	}

	return (NULL);
}
